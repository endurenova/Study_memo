<?php
echo date("Y");  // 2023
echo date("y");  // 23

echo date("M");  // Jan ~ Dec
echo date("m");  // 01 ~ 12
echo date("n");  // 1 ~ 12

echo date("D");  // Mon ~ Sun
echo date("N");  // 1(Mon) ~ 7(Sun)

echo date("d");  // 01 ~ 31
echo date("j");  // 1 ~ 31

echo date("Y-m-d H:i:s");  // 2023-11-25 11:17:03

?>